    private void jcSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcSubjectActionPerformed
         jcExamId.removeAllItems();
        if(jcSubject.getSelectedIndex()==0){
           JOptionPane.showMessageDialog(null,"Please Select an Subject","Paper Selection",JOptionPane.ERROR_MESSAGE);
           return;
       }
      try{
       String paperName = jcSubject.getSelectedItem().toString().trim();
       if(!ExamDAO.isPaperSet(paperName)){
           JOptionPane.showMessageDialog(null,"No Question Paper Found",paperName+" Paper",JOptionPane.INFORMATION_MESSAGE);
           jcSubject.setSelectedIndex(0);
           return;    
        }
       ArrayList<String> examidList = ExamDAO.getExamIdbySubject( UserProfile.getUsername().trim(), paperName);
       if(examidList.isEmpty()){
          JOptionPane.showMessageDialog(null,"You Have given all "+paperName+" Exams","All Paper Atempted",JOptionPane.INFORMATION_MESSAGE);
          return;  
       }
       for(String id : examidList){
           jcExamId.addItem(id);
       }
      }
      catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Error in Fetching Exam and Performance Table","Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
    }//GEN-LAST:event_jcSubjectActionPerformed

