    private void btnTakeTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTakeTestActionPerformed
     if(jcSubject.getSelectedIndex()==0||jcExamId.getSelectedItem().toString().isEmpty()){
           JOptionPane.showMessageDialog(null,"Please Select an Subject And Exam-Id ","Paper Selection",JOptionPane.ERROR_MESSAGE);
           return;
       }
      String paperName = jcSubject.getSelectedItem().toString().trim();
      String examid = jcExamId.getSelectedItem().toString().trim();
      int res = JOptionPane.showConfirmDialog(null,"Subject : "+paperName+"\n Exam-Id : "+examid,"Confirm",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
      try{
          if(JOptionPane.YES_OPTION==res){
            int noOfQues = ExamDAO.getNoOfQues(examid);
            Exam ex = new Exam(examid , paperName , noOfQues);
            TakeTestFrame testFrame = new TakeTestFrame(ex);
            testFrame.setVisible(true);
            this.dispose();
           } 
        }
      catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Error in Fetching No of Question from Exam Table","Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
    }//GEN-LAST:event_btnTakeTestActionPerformed

